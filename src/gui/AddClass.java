    public void loadTeacher() {

        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `teacher`");

            Vector<String> v = new Vector<>();
            v.add("Select Teacher");
            while (rs.next()) {
                String fullName = rs.getString("fname") + " " + rs.getString("lname");
                v.add(fullName);
                teacherMap.put(fullName, rs.getString("teacher.Tno"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadSubject() {

        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `subject`");

            Vector<String> v = new Vector<>();
            v.add("Select Subject");
            while (rs.next()) {

                v.add(rs.getString("name"));
                subMap.put(rs.getString("name"), rs.getString("subject.id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox2.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadClass() {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `class` "
                    + "INNER JOIN `teacher` ON `class`.`subject_id`=`teacher`.`subject_id` "
                    + "INNER JOIN `subject` ON `class`.`subject_id`=`subject`.`id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> v = new Vector<>();
                v.add(resultSet.getString("id"));
                v.add(resultSet.getString("name")); // Assuming the column name is 'name' in the 'subject' table
                v.add(resultSet.getString("Fname") + " " + resultSet.getString("lname")); // Assuming 'Fname' and 'lname' columns are in 'teacher' table
                v.add(resultSet.getString("date"));
                v.add(resultSet.getString("time"));
                v.add(resultSet.getString("link"));

                model.addRow(v);
            }

            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

