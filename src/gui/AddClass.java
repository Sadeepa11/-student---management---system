    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String teacher = String.valueOf(jComboBox1.getSelectedItem());
        String subject = String.valueOf(jComboBox2.getSelectedItem());
        String date = new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate());
        String time = jLabel5.getText();
        String link = jTextField1.getText();

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `class` WHERE `teacher_Tno`='" + teacherMap.get(teacher) + "'"
                    + " AND `subject_id`='" + subMap.get(subject) + "' "
                    + "AND `date`='" + date + "' AND `time`='" + time + "' AND `link`='" + link + "'");
            if (resultSet.next()) {

                JOptionPane.showMessageDialog(this, "Class already Added", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                if (teacher.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please select a teacher", "Warning", JOptionPane.WARNING_MESSAGE);

                } else if (subject.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please select a subject", "Warning", JOptionPane.WARNING_MESSAGE);

                } else if (time.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please set time", "Warning", JOptionPane.WARNING_MESSAGE);

                } else if (link.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please enter the class link", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    MySQL.execute("INSERT INTO `class`(`teacher_Tno`,`link`,`date`,`subject_id`,`time`)"
                            + "VALUES('" + teacherMap.get(teacher) + "','" + link + "','" + date + "','" + subMap.get(subject) + "','" + time + "')");
                    loadClass();
                    JOptionPane.showMessageDialog(this, "Succesfully added the class", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

